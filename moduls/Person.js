const mongoose = require("mongoose");

const personData = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  age: {
    type: Number,
  },
  work: {
    type: String,
    enum: ["chef", "waiter", "manager"],
    required: true,
  },
  mobile: {
    type: Number,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  address: {
    type: String,
  },
  salry: {
    type: Number,
    required: true,
  },
});
const Person = mongoose.model("Person", personData);

module.exports = Person;
